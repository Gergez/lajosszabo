pontjainak tisztázása spontánabb beszélgetésekben folytatódott; ám a vita során később
mind a három gondolkodó többször is újból hosszabb, írásban elkészített hozzászólásban
fejtette ki álláspontját. Az álláspontok tisztázódásával „a felületi ellentmondások”
fokozatosan „relativálódtak”, „mélyebb ellentmondásokra vezetődtek vissza”. Tehát nem a
viták dacára, hanem éppen azoknak köszönhetően rendkívül ritka, valódi szellemi közösség
jött létre a negyvenes években egy „üdvtörténeti” pillanatra a Haris-közi trialógusok során. (Tábor Ádám)
